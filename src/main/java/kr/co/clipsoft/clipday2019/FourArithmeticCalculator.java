package kr.co.clipsoft.clipday2019;

public class FourArithmeticCalculator {
    public long add(int op1, int op2) {
        return op1 + op2;
    }

    public long minus(int op1, int op2) {
        return op1 - op2;
    }

    public long multiply(int op1, int op2) {
        return op1 * op2;
    }
}